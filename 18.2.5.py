# Создаем массив с именами коллег
coworkers = ["Иван", "Мария", "Сергей", "Елена", "Дмитрий", "Анна"]

# Выводим имя первого и последнего коллеги
print("Первый коллега:", coworkers[0])
print("Последний коллега:", coworkers[-1])

# Выводим имена каждого первого и каждого второго коллеги
print("Каждый первый коллега:", coworkers[::2])
print("Каждый второй коллега:", coworkers[1::2])

# Выводим количество коллег
print("Количество коллег:", len(coworkers))

# Считываем имя нового коллеги с консоли
new_coworker = input("Введите имя нового коллеги: ")

# Добавляем нового коллегу в массив
coworkers.append(new_coworker)

# Выводим полученный список и его длину
print("Список коллег:", coworkers)
print("Длина списка:", len(coworkers))

# Считываем имя для проверки с консоли
name_to_check = input("Введите имя для проверки: ")

# Проверяем, работает ли человек с вами
if name_to_check in coworkers:
    print(f"{name_to_check} работает с вами.")
else:
    print(f"{name_to_check} не работает с вами.")