# Создаем двумерный массив для ежедневника (7 дней x 3 временных интервала)
todo_list = [
    ['', '', ''],  # Понедельник
    ['', '', ''],  # Вторник
    ['', '', ''],  # Среда
    ['', '', ''],  # Четверг
    ['', '', ''],  # Пятница
    ['', '', ''],  # Суббота
    ['', '', '']   # Воскресенье
]

days = ["Понедельник", "Вторник", "Среда", "Четверг", "Пятница", "Суббота", "Воскресенье"]
time_slots = ["Утро", "День", "Вечер"]

# Заполняем ежедневник значениями, которые вводит пользователь
print("Заполните ежедневник:")
for i in range(7):  # Цикл по дням недели
    print(f"--- {days[i]} ---")
    for j in range(3):  # Цикл по временным интервалам
        todo_list[i][j] = input(f"  {time_slots[j]}: ")

# Выводим ежедневник после заполнения
print("\nВаш заполненный ежедневник:")
for i in range(7):
    print(f"{days[i]}: Утро - {todo_list[i][0]}, День - {todo_list[i][1]}, Вечер - {todo_list[i][2]}")

# Удаляем одну запись
try:
    delete_day = int(input("\nВведите номер дня недели (1-7) для удаления записи: ")) - 1
    delete_time = int(input("Введите номер временного интервала (1-3) для удаления записи: ")) - 1

    if 0 <= delete_day < 7 and 0 <= delete_time < 3:
        todo_list[delete_day][delete_time] = ''  # Очищаем запись
        print("Запись удалена.")
    else:
        print("Ошибка: Неверный номер дня недели или временного интервала.")

except ValueError:
    print("Ошибка: Введите целое число.")


# Добавляем новую запись
try:
    add_day = int(input("\nВведите номер дня недели (1-7) для добавления записи: ")) - 1
    add_time = int(input("Введите номер временного интервала (1-3) для добавления записи: ")) - 1

    if 0 <= add_day < 7 and 0 <= add_time < 3:
        new_record = input("Введите новую запись: ")
        todo_list[add_day][add_time] = new_record
        print("Запись добавлена.")
    else:
        print("Ошибка: Неверный номер дня недели или временного интервала.")

except ValueError:
    print("Ошибка: Введите целое число.")

# Выводим обновленный ежедневник
print("\nОбновленный ежедневник:")
for i in range(7):
    print(f"{days[i]}: Утро - {todo_list[i][0]}, День - {todo_list[i][1]}, Вечер - {todo_list[i][2]}")